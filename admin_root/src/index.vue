<script lang="babel">
  import store from './store'
  export default {
    data () {
      return {
        realName: '',
        position: '',
        lastLoginTime: ''
      }
    },
    components: {},
    beforeRouteEnter (to, from, next){
      store.dispatch('actions_users_getInfo').then(function (data) {
        next((vm) => {
          if (data.status === 0) {
            vm.realName = data.realName;
            vm.position = data.position;
            vm.lastLoginTime = data.lastLoginTime;
          }
        });
      });
    },
    // 首次创建会判断是否在移动端访问
    created () {
      const sUserAgent = navigator.userAgent.toLowerCase();
      const bIsIpad = sUserAgent.match(/ipad/i) == "ipad";
      const bIsIphoneOs = sUserAgent.match(/iphone os/i) == "iphone os";
      const bIsMidp = sUserAgent.match(/midp/i) == "midp";
      const bIsUc7 = sUserAgent.match(/rv:1.2.3.4/i) == "rv:1.2.3.4";
      const bIsUc = sUserAgent.match(/ucweb/i) == "ucweb";
      const bIsAndroid = sUserAgent.match(/android/i) == "android";
      const bIsCE = sUserAgent.match(/windows ce/i) == "windows ce";
      const bIsWM = sUserAgent.match(/windows mobile/i) == "windows mobile";
      if (bIsIpad || bIsIphoneOs || bIsMidp || bIsUc7 || bIsUc || bIsAndroid || bIsCE || bIsWM) {
        $("a[href!='#']").attr("data-toggle", "offcanvas");
      }
    },
    methods: {
      logout: function () {
        this.$http.post('/api/logout').then(() => {
          this.$store.dispatch('actions_users_clearInfo');
          this.$router.push('/login');
        }, (err) => {
          console.log(err);
        })
      }
    }
  }
</script>
